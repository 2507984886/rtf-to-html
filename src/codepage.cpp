// ============================================================
// cpToUtf8 实现
// ============================================================
std::string cpToUtf8(unsigned char byte, int codepage) {
    // ASCII 范围直接返回
    if (byte < 0x80) {
        return std::string(1, static_cast<char>(byte));
    }

    uint32_t codepoint = 0;

    switch (codepage) {
        case 1252: {
            if (byte >= 0x80 && byte <= 0x9F) {
                codepoint = cp1252_high[byte - 0x80];
            } else {
                // 0xA0-0xFF 与 Latin-1 相同
                codepoint = byte;
            }
            break;
        }
        case 1250:
            codepoint = cp1250_map[byte - 0x80];
            break;
        case 1251:
            codepoint = cp1251_map[byte - 0x80];
            break;
        case 1253:
            codepoint = cp1253_map[byte - 0x80];
            break;
        case 1254:
            codepoint = cp1254_map[byte - 0x80];
            break;
        case 1255:
            codepoint = cp1255_map[byte - 0x80];
            break;
        case 1256:
            codepoint = cp1256_map[byte - 0x80];
            break;
        case 1257:
            codepoint = cp1257_map[byte - 0x80];
            break;
        case 1258:
            codepoint = cp1258_map[byte - 0x80];
            break;
        case 850:
            codepoint = cp850_map[byte - 0x80];
            break;
        case 437:
            codepoint = cp437_map[byte - 0x80];
            break;
        default:
            // 未知代码页，使用 CP1252 作为回退
            if (byte >= 0x80 && byte <= 0x9F) {
                codepoint = cp1252_high[byte - 0x80];
            } else {
                codepoint = byte;
            }
            break;
    }

    // 处理无效码点
    if (codepoint == 0xFFFD) {
        return "\xEF\xBF\xBD"; // UTF-8 替换字符
    }

    return utf32ToUtf8(codepoint);
}

